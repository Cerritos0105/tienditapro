    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nombre  = txtNombre.getText();
        String Apellido = txtApellido.getText();
        String User = txtUser.getText();
        String Pass = txtPass.getText();
        String Ciudad = txtCiudad.getText();
        String fecha = txtFecha.getText();
        String cargo = (String) CMBcargo.getSelectedItem();
        String telefono = txtTelefono.getText();
        LogonApi log = new LogonApi();
        AgregarUser au = new AgregarUser();
        if(nombre.isEmpty()|| Apellido.isEmpty() || User.isEmpty() || Pass.isEmpty() || Ciudad.isEmpty() || fecha.isEmpty() || cargo.isEmpty() || telefono.isEmpty()){
           lblerror.setText("Favor de llenar todos los campos");
        }else if(log.login2(User)== true){
            lblerror.setText("Ese usuario ya existe");
        }else{
            au.Agrgar(nombre, Apellido, User, Pass, Ciudad, fecha, cargo, telefono);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

